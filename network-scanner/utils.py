def normalize_mac(mac_str):
    """
    Standardizes a MAC address string into lowercase colon-separated format (e.g. 00:11:22:33:44:55).
    Handles stripped leading zeros commonly found in macOS arp outputs (e.g. 8:5b:e:4f:b3:12).
    """
    if not mac_str:
        return "unknown"
    
    # Strip any brackets or parens, replace dashes with colons
    cleaned = mac_str.strip("()[] ").replace("-", ":")
    parts = cleaned.split(":")
    
    # Standard MAC addresses have 6 octets
    if len(parts) == 6:
        normalized_parts = []
        for part in parts:
            # Pad each octet to ensure 2 characters (e.g. "8" -> "08")
            normalized_parts.append(part.zfill(2).lower())
        return ":".join(normalized_parts)
    
    return cleaned.lower()

def lookup_vendor(mac_address):
    """
    Retrieves the manufacturer/vendor for a given MAC address.
    First checks the local COMMON_OUIS mapping, then falls back to api.macvendors.com with a timeout.
    """
    normalized = normalize_mac(mac_address)
    if normalized == "unknown":
        return "Unknown"
    
    # Extract prefix (first 3 octets, e.g. "00:11:22")
    prefix = ":".join(normalized.split(":")[:3])
    
    # Step 1: Check local cache dictionary
    if prefix in COMMON_OUIS:
        return COMMON_OUIS[prefix]
    
    # Step 2: Try online API with a short timeout to prevent blocking
    try:
        url = f"https://api.macvendors.com/{urllib.parse.quote(normalized)}"
        req = urllib.request.Request(
            url, 
            headers={"User-Agent": "Mozilla/5.0 (Network Scanner Utility)"}
        )
        with urllib.request.urlopen(req, timeout=1.5) as response:
            vendor = response.read().decode("utf-8").strip()
            return vendor
    except (urllib.error.URLError, urllib.error.HTTPError, TimeoutError, Exception):
        # Fall back to "Unknown" if offline, timed out, or rate-limited
        return "Unknown"

def export_to_csv(filepath, devices):
    """
    Exports the scanned devices to a CSV file.
    devices: list of dicts with keys: 'ip', 'mac', 'hostname', 'vendor'
    """
    headers = ["IP Address", "MAC Address", "Hostname", "Vendor"]
    try:
        with open(filepath, mode="w", newline="", encoding="utf-8") as csv_file:
            writer = csv.writer(csv_file)
            writer.writerow(headers)
            for dev in devices:
                writer.writerow([
                    dev.get("ip", ""),
                    dev.get("mac", ""),
                    dev.get("hostname", ""),
                    dev.get("vendor", "")
                ])
        return True, f"Successfully exported to {filepath}"
    except Exception as e:
        return False, f"Failed to export: {str(e)}"
